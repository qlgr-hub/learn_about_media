static inline const char* fferr2str(int errCode)
{
     char tmp[AV_ERROR_MAX_STRING_SIZE] = {0};
     return av_make_error_string(tmp, AV_ERROR_MAX_STRING_SIZE, errCode);
}


// simple console logger
void Err(std::string_view msg, std::optional<int> errCode /*= std::nullopt*/, const char* file /*= __FILE__*/, int line /*= __LINE__*/) {
    if (errCode) {
        fprintf(stderr, "[%s:%d] %s (%s)\n", file, line, msg.data(), fferr2str(errCode.value()));
    }
    else {
        fprintf(stderr, "[%s:%d] %s\n", file, line, msg.data());
    }
}


